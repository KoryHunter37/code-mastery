# abs(x)

# Return the absolute value of a number. 
# The argument may be an integer or a floating point number.

# > 5
print(abs(-5))

# > 5
print(abs(5))

# > 2 2
# Regardless of order, will always print the "difference" between two numbers. 
print(abs(7 - 9), abs(9-7))

# > 4 4
# Can find the "difference" between negative and positive numbers.
print(abs((-2) - 2), abs(2 - (-2)))