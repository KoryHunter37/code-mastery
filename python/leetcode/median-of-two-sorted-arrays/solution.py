# https://leetcode.com/problems/median-of-two-sorted-arrays/

from statistics import median
class Solution:
    def findMedianSortedArrays(self, nums1, nums2):
        """
        :type nums1: List[int]
        :type nums2: List[int]
        :rtype: float
        """
        nums = sorted(nums1 + nums2)
        return median(nums)